public class A 
{

	public double sum;
	A()
	{
		int a = 1;
		int b = 2;
		
		sum = a+b;
	}
	
	A(double a, double b)
	{
		sum = a+b;
	}
	
	A(int a, int b)
	{
		sum = a+b;
	}
	
	
	public void display(String v)
	{
		System.out.println(v +"="+sum);
	}
	
	public static void main(String args[])
	{
		A y = new A();
		A x = new A(1,5);
		A z = new A(1.10,2.20);
		y.display("y");
		x.display("x");
		z.display("z");
	}
}
